// 삼항 조건 연산자 Ternary Operator
// Conditional ? true statement : false statement

let a = 10;

a === 10 ? console.log('a is ten') : console.log('nope');

let fruit = 'apple';

fruit === 'apple' ? console.log('apple') : console.log('not apple');

let emoji = fruit === 'apple' ? 'apple' : 'no';

console.log(emoji);
