// Math
// static properties, method

console.log(Math.E); // 오일러 상수
console.log(Math.PI);

// static method
console.log(Math.abs(-10));

console.log(Math.ceil(1.4)); //올림
console.log(Math.floor(1.4)); // 내림
console.log(Math.round(1.4)); // 반올림
console.log(Math.round(1.5));
console.log(Math.trunc(1.5432)); // 정수만 반환

// 최대, 최소값
console.log(Math.max(1, 3, 5));
console.log(Math.min(2, -1, 3));

// 거듭제곱
console.log(3 ** 2);
console.log(Math.pow(3, 2));

// 제곱근
console.log(Math.sqrt(9));

// 랜덤한 값을 반환
console.log(Math.random()); // 0 ~ 1

// 1 ~ 10
console.log(Math.random() * 10 + 1);
console.log(Math.floor(Math.random() * 10 + 1));
